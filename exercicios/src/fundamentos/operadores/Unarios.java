package fundamentos.operadores;

public class Unarios {
	public static void main(String[] args) {
		int a = 1;
		int b = 2;
		
		a++; //a = a + 1
		a--; //a = a - 1
		
		/*
		 * Esse sempre vai ter prioridade, ou seja vai ser efetuado primeiro a operação para depois comparar.
		 */
		++b; //b = b + 
		 
		--b; //b = b - 1 
		
	}
}
