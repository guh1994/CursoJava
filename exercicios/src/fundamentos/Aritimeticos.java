package fundamentos;

public class Aritimeticos {
	public static void main(String[] args) {
		System.out.println(2 + 3);
		
		var x = 34.56;
		double y = 2.2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 8;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a / (double) b);
		
		System.out.println(8 % 3);//Operador modulo é representado por % que é o resto da divisão
		System.out.println(a % b);//Operador modulo é representado por % que é o resto da divisão com uso de variaveis
		
	}
}
